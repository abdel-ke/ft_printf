#include "ft_printf.h"

int		ft_point(char *str, t_flags *t)
{
	int i;

	i = 0;
	while (str[i] != '.' && !ft_type(str[i]))
		i++;
	if (str[i] == '.')
		t->point = 1;
	return (i);
}
